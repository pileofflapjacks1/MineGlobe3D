/** Normalize silver Moz-equivalent to koz for sizing comparisons */
export function productionToKozEquiv(production: number, unit: string, metal: string): number {
  if (unit === 'Moz') return production * 1000;
  if (unit === 'kt' && metal === 'copper') return production * 0.15; // rough visual weight only
  if (unit === 'tonnes') return production * 32.15; // oz → koz-ish visual
  return production;
}

export function getSiteMetrics(siteId: string): SiteMetric[] {
  return siteMetrics.filter((m) => m.siteId === siteId);
}

export function getLatestSiteMetrics(siteId: string, preferredPeriod = 'Q1 2026'): SiteMetric[] {
  const all = getSiteMetrics(siteId);
  const preferred = all.filter((m) => m.period === preferredPeriod);
  if (preferred.length) return preferred;
  // fallback: latest period lexicographically among available
  const periods = [...new Set(all.map((m) => m.period))].sort();
  const last = periods[periods.length - 1];
  return all.filter((m) => m.period === last);
}

export function getSiteVolumeScore(siteId: string, period: string): number {
  const metrics = siteMetrics.filter((m) => m.siteId === siteId && m.period === period);
  if (!metrics.length) {
    // try any period
    const any = siteMetrics.filter((m) => m.siteId === siteId);
    return any.reduce(
      (sum, m) => sum + productionToKozEquiv(m.production, m.unit, m.metal),
      0,
    );
  }
  return metrics.reduce(
    (sum, m) => sum + productionToKozEquiv(m.production, m.unit, m.metal),
    0,
  );
}

export function getSitesForCompany(companyId: string): MiningSite[] {
  return miningSites.filter((s) => s.companyIds.includes(companyId));
}

export function getSitesForCountry(countryId: string): MiningSite[] {
  return miningSites.filter((s) => s.countryId === countryId);
}

export function getCompaniesForCountry(countryId: string): Company[] {
  const ids = new Set(
    miningSites.filter((s) => s.countryId === countryId).flatMap((s) => s.companyIds),
  );
  return companies.filter((c) => ids.has(c.id));
}

export function getCompanyAggregates(companyId: string): CompanyAggregate[] {
  return companyAggregates.filter((a) => a.companyId === companyId);
}

